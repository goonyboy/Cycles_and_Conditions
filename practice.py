# Условие задачи: записать условие проверки целого числа А,
# является ли оно кратным двум или трём.

A = int(input('Введите число: '))

if (A % 2 == 0) or (A % 3 == 0):
    print('Число кратно 2 или 3')
else:
    print('Число не кратно 2 или 3')


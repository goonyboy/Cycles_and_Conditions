# Записать выражение, которое вернет True, когде каждое из чисел A и B нечетное

A = int(input('Введите число A: '))
B = int(input('Введите число B: '))

if (A % 2 != 0) and (B % 2 != 0):
    print('True')
else:
    print('False')

